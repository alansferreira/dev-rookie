console.log('contagem crescente')
for (let i = 0; i < 5; i++) {
  console.log(i);
}
console.log('contagem decrescente')
for (let i = 5; i > 0; i--) {
  console.log(i);
}

console.log('contagem crescente até 5')
for (let i = 0; i <= 5; i++) {
  console.log(i);
}

console.log('contagem decrescente até 0')
for (let i = 5; i >= 0; i--) {
  console.log(i);
}

